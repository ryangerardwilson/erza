create table if not exists public.circles (
  name text primary key,
  rhythm text not null,
  sort_order integer not null default 0
);

create table if not exists public.profiles (
  handle text primary key,
  display_name text not null,
  bio text not null,
  primary_circle text not null references public.circles(name),
  followers integer not null default 0,
  following boolean not null default false,
  created_at timestamptz not null default now()
);

create table if not exists public.accounts (
  handle text primary key references public.profiles(handle) on delete cascade,
  password_hash text not null,
  created_at timestamptz not null default now()
);

create table if not exists public.posts (
  id bigint generated by default as identity primary key,
  slug text not null unique,
  author_handle text not null references public.profiles(handle) on delete cascade,
  author_name text not null,
  circle_name text not null references public.circles(name),
  body text not null,
  likes integer not null default 0,
  boosts integer not null default 0,
  tags text[] not null default '{}'::text[],
  created_at timestamptz not null default now()
);

create table if not exists public.replies (
  id bigint generated by default as identity primary key,
  thread_slug text not null references public.posts(slug) on delete cascade,
  author_handle text not null references public.profiles(handle) on delete cascade,
  author_name text not null,
  body text not null,
  created_at timestamptz not null default now()
);

create table if not exists public.follow_relations (
  follower_handle text not null references public.profiles(handle) on delete cascade,
  followed_handle text not null references public.profiles(handle) on delete cascade,
  created_at timestamptz not null default now(),
  primary key (follower_handle, followed_handle),
  constraint no_self_follow check (follower_handle <> followed_handle)
);

create or replace function public.normalize_koinonia_handle(raw text)
returns text
language sql
immutable
as $$
  select coalesce(
    nullif(
      regexp_replace(lower(ltrim(coalesce(raw, ''), '@')), '[^a-z0-9_]+', '', 'g'),
      ''
    ),
    'resident'
  );
$$;

create or replace function public.ensure_koinonia_profile(author_name text, profile_handle text)
returns void
language plpgsql
as $$
declare
  normalized_handle text := public.normalize_koinonia_handle(profile_handle);
  normalized_name text := btrim(coalesce(author_name, ''));
begin
  if normalized_name = '' then
    normalized_name := initcap(normalized_handle);
  end if;

  insert into public.profiles (handle, display_name, bio, primary_circle, followers, following)
  values (
    normalized_handle,
    normalized_name,
    'Joined through the Koinonia terminal prototype.',
    'proto-lab',
    1,
    false
  )
  on conflict (handle) do nothing;
end;
$$;

create or replace function public.create_dispatch(author_name text, profile_handle text, body text)
returns void
language plpgsql
as $$
declare
  normalized_handle text := public.normalize_koinonia_handle(profile_handle);
  normalized_name text := btrim(coalesce(author_name, ''));
  normalized_body text := btrim(coalesce(body, ''));
  base_slug text;
  candidate_slug text;
  suffix integer := 1;
begin
  if normalized_name = '' or normalized_body = '' then
    raise exception 'Display name and dispatch are required.';
  end if;

  perform public.ensure_koinonia_profile(normalized_name, normalized_handle);

  base_slug := trim(both '-' from regexp_replace(lower(left(normalized_body, 36)), '[^a-z0-9]+', '-', 'g'));
  if base_slug = '' then
    base_slug := 'dispatch';
  end if;

  candidate_slug := base_slug;
  while exists(select 1 from public.posts where slug = candidate_slug) loop
    suffix := suffix + 1;
    candidate_slug := base_slug || '-' || suffix::text;
  end loop;

  insert into public.posts (slug, author_handle, author_name, circle_name, body, tags)
  values (
    candidate_slug,
    normalized_handle,
    normalized_name,
    'proto-lab',
    normalized_body,
    array['prototype', 'koinonia']
  );
end;
$$;

create or replace function public.add_thread_reply(thread_slug text, author_name text, profile_handle text, body text)
returns void
language plpgsql
as $$
declare
  normalized_handle text := public.normalize_koinonia_handle(profile_handle);
  normalized_name text := btrim(coalesce(author_name, ''));
  normalized_body text := btrim(coalesce(body, ''));
begin
  if normalized_name = '' or normalized_body = '' then
    raise exception 'Both name and reply text are required.';
  end if;

  if not exists(select 1 from public.posts where slug = add_thread_reply.thread_slug) then
    raise exception 'Thread does not exist.';
  end if;

  perform public.ensure_koinonia_profile(normalized_name, normalized_handle);

  insert into public.replies (thread_slug, author_handle, author_name, body)
  values (thread_slug, normalized_handle, normalized_name, normalized_body);
end;
$$;

create or replace function public.increment_post_like(post_id bigint)
returns void
language sql
as $$
  update public.posts
  set likes = likes + 1
  where id = increment_post_like.post_id;
$$;

create or replace function public.increment_post_boost(post_id bigint)
returns void
language sql
as $$
  update public.posts
  set boosts = boosts + 1
  where id = increment_post_boost.post_id;
$$;

drop function if exists public.toggle_profile_follow(text);

create or replace function public.toggle_profile_follow(viewer_handle text, profile_handle text)
returns boolean
language plpgsql
as $$
declare
  normalized_viewer text := public.normalize_koinonia_handle(viewer_handle);
  normalized_target text := public.normalize_koinonia_handle(profile_handle);
  already_following boolean;
begin
  if normalized_viewer = normalized_target then
    return false;
  end if;

  select exists(
    select 1
    from public.follow_relations
    where follower_handle = normalized_viewer
      and followed_handle = normalized_target
  ) into already_following;

  if already_following then
    delete from public.follow_relations
    where follower_handle = normalized_viewer
      and followed_handle = normalized_target;

    update public.profiles
    set followers = greatest(followers - 1, 0)
    where handle = normalized_target;

    return false;
  end if;

  insert into public.follow_relations (follower_handle, followed_handle)
  values (normalized_viewer, normalized_target)
  on conflict do nothing;

  update public.profiles
  set followers = followers + 1
  where handle = normalized_target;

  return true;
end;
$$;

create or replace view public.feed_timeline_view as
select
  p.id,
  p.slug,
  p.author_name as author,
  p.author_handle as handle,
  p.circle_name as circle,
  p.body,
  p.likes,
  p.boosts,
  coalesce(r.reply_count, 0)::integer as reply_count,
  p.created_at
from public.posts p
left join (
  select thread_slug, count(*)::integer as reply_count
  from public.replies
  group by thread_slug
) r on r.thread_slug = p.slug;

create or replace view public.network_overview_view as
select
  (select count(*)::integer from public.posts) as posts,
  (select count(*)::integer from public.circles) as circles,
  (select count(*)::integer from public.profiles) as people;

create or replace view public.signals_view as
select
  tag,
  count(*)::integer as count
from public.posts,
lateral unnest(tags) as tag
group by tag;

